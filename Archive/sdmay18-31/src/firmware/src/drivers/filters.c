/**
 * Get the filter values for a given analog filter
 *
 * @param filter the filter requested
 *
 * @return pointer to the filter values
 */
const filter_t* getFilter(uint8_t filter)
{
    switch(filter)
    {
        case FILTER_1ST_ORDER_LP:

            return filter_character_firstLPF;
            break;

        case FILTER_5TH_ORDER_LP:

            return filter_character_fifthLPF;
            break;

        case FILTER_1ST_ORDER_HP:

            return filter_character_firstHPF;
            break;

        case FILTER_5TH_ORDER_HP:

            return filter_character_fifthHPF;
            break;

        case FILTER_2ND_ORDER_BP:

            return filter_character_secondBPF;
            break;

        case FILTER_6TH_ORDER_BP:

            return filter_character_sixthBPF;
            break;

        default:
            return 0;
            break;
    }
}